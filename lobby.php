function getDirContents($path) {
  $rii = new RecursiveIteratorIterator(new RecursiveDirectoryIterator($path));
  $files = array(); 
  foreach ($rii as $file)
      if (!$file->isDir())
          $files[] = $file->getFilename();
  return $files;
}

